#運算及變數索引功能
#請給我一個整數(integer)向量，共有7個數字，前兩個數字分別為1、X，第三個數字（奇數）為前兩個相加，第四個數字（偶數）為前兩個數字相乘，第五個數字（奇數）為前兩個相加，依此類推。
#– 註，每個人的X不一樣(以下範例數字為3的範例)，由老師指定

#以下範例數字為3的範例：
y = c(1, 3)
y[3] = y[1] + y[2]
y[4] = y[2] * y[3]
y[5] = y[3] + y[4]
y[6] = y[4] * y[5]
y[7] = y[5] + y[6]
